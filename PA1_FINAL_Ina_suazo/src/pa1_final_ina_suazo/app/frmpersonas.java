            private Personas getPersonaFormulario() {
            try {
            String identidad = txtidentidad.getText();
            String nombre = txtnombre.getText();
            String apellido = txtapellido.getText();
            double peso = Double.parseDouble(txtpeso.getText());
            double pesoequipo=Double.parseDouble(txtpesoequipaje.getText());
            int anionacimiento=Integer.parseInt(txtanionacimiento.getText());

            Personas persona = new Personas(identidad);
            persona.setNombre(nombre);
            persona.setApellido(apellido);
            persona.setPeso(peso);
            persona.setPesoequipaje(pesoequipo);
            persona.setAnionacimiento(anionacimiento);

            
            return persona;
            
            
             
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al leer formulario " + e.toString());
        }

        return null;
        
        }
    
